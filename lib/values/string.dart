فلسفه( ارزش ها و باورها):
پزشک عمومی بعنوان صف اول در ارائه خدمات سلامت با جامعه قرار دارد. بنابراین دائما در معرض قضاوت و ارزیابی جامعه قرار گرفته باید ضمن داشتن ویژگیهای مطلوب حرفه ای و مهارتهای تخصصی و هنجارهای جامعه را مراعات کند.
انسان موجودی چند بعدی با دغدغه ها و نیازهای متنوع جسمی–روحی و فرهنگی است. این امر موجب می شود برنامه های آموزش نیروهای انسانی برای تامین سلامت انسان دارای ارزشهای چند لایه و متنوعی باشد. پزشک عمومی نمی تواند صرفاً به جسم و بیماریهای آن بپردازد. پزشک تنها شخصی است که به عنوان صاحب یک حرفه می تواند به خصوصی ترین حریم انسان های نیازمند خدمات سلامت راه یابد به طوری که گاه به اسرار بیمار از نزدیک ترین همراهان وی بیشتر آگاه است. بنابراین مهمترین ارزش و رکن کلیدی در برنامه های پزشکی عمومی باید تربیت پزشکانی متعهد به اخلاق و منش حرفه ای باشد. دانش پزشکی قلمرویی در حال تغییر دائمی است و پزشک نیازمند آن است که برای اطمینان از صحت تصمیم گیری های بالینی و ارائه خدمات و انطباق این اقدامات با شواهد معتبر، یادگیری مادام­العمر و ارتقای حرفه­ای مستمر را سرلوحه فعالیت حرفه ای خود قرار دهد. از این رو، پرورش تفکر انتقادی و مهارتهای یادگیری خودراهبر، یکی از ضروریات برنامه پزشکی عمومی به شمار می رود که به خصوص در راهبردهای اجرای برنامه بایستی مورد توجه قرار گیرد.
بر اساس اصول تعلیم و تربیت، طراحی آموزشی مناسب و سازماندهی محتوا در عرصه های یادگیری امری ضروری است. تقویت تعامل میان استاد و فراگیر، تماس زودرس و هدفمند دانشجو با محیط بالینی، همچنین تعریف فرصت تمرین و یادگیری مهارتها و محول نمودن مسئولیت بیشتر به دانشجویان متناسب با مرحله آموزش(علوم پایه، مقدمات بالینی، کارآموزی و کارورزی) توأم با روشهای اطمینان از ایمنی و حفظ حقوق بیماران در برنامه مورد تاکید قرار گرفته است.
دورنما(چشم انداز):
برنامه دوره پزشکی عمومی با استفاده از آخرین یافته های آموزش پزشکی خواهد توانست ضمن حفظ قابلیت اجرا توسط دانشکده های کمتر برخوردار، به تحقق استانداردهای جهانی مورد قبول برنامه آموزش پزشکی در ایران کمک کند و در هر حال، با اجرای آن دانش آموختگانی متعهد، توانمند و پاسخگو در برابر نیازهای نظام سلامت ایران به جامعه تقدیم شود.
رسالت (مأموریت):
رسالت برنامه درسی پزشکی عمومی، تبیین اهداف، فرصتهای یادگیری، و قواعدی است که تکمیل و اجرای آن در دانشکده ها به تحقق توانمندیهای مورد انتظار از دانش آموختگان این دوره بیانجامد. این برنامه ضمن توجه به دغدغه های کلیه ذینفعان برنامه، اهداف آموزشی را با رویکردی عملگرایانه و منعطف به نحوی ارائه میکند که کلیه دانشگاههای کشور بتوانند با در نظر گرفتن تمامی منابع و ویژگیهای آموزشی خود، تربیت پزشکان عمومی را با حداکثر انطباق با برنامه ملی اجرا نمایند.
ما بر اين باوريم كه دانش آموختگان اين رشته بايد با استفاده از دانش و مهارتهاي روزآمد در زمينه پزشكي بگونه­اي تربيت شوند كه بتوانند به عنوان اولين نقطه تماس مردم با نظام مراقبت سلامت[1] ايفاي وظيفه نمايند. فارغ التحصيلان اين برنامه بايد بتوانند نقش حرفه­اي خود را يا از طريق ارائه مستقيم و مطلوب(روزآمد) خدمات به بيماران و يا از طريق هماهنگ سازي خدمات با ساير ارايه كنندگان خدمات و به فراخور نيازهاي سلامت و منابع موجود در جامعه ارائه نموده و از اين طريق به تحقق نظام ارائه خدمات ادغام يافته[2]كمك نمايند. مراقبت ارائه شده توسط اين فارغ التحصيلان بدون در نظر گرفتن سن، جنس، نژاد و يا سطح فرهنگي و اجتماعي و با در نظر گرفتن پيش زمينه­هاي فرهنگي، اجتماعي، اقتصادي و روانشناختي بيماران بوده و بايد در حد امكان مداوم، همه جانبه و فراگير، در جهت ارتقاء سلامت آحاد جامعه باشد. همچنين اين دانشجويان بايد بگونه­ايي تربيت شوند كه بتوانند به شناسايي مشكلات جامعه كه فراتر از مشكلات افراد مراجعه كننده و متقاضي خدمات سلامت است، بپردازند و با آگاهي و درك صحيح از رفتارهاي مرتبط با سلامت در جامعه نقش موثرتری در حمايت از تلاشها و حركتهاي اجتماعي كه جامعه در جهت محافظت از سلامت آحاد خود انجام مي­دهد، ایفا کند.
ما بر اين باوريم كه دانش آموختگان اين رشته افرادي مسئوليت پذير، دلسوز، انساندوست و خود توانمند ساز بوده و در ارتقاي سلامت جامعه با تمام جديت و متعهدانه فعاليت مي­نمايند. دانشكده های پزشكي به عنوان مجریان این دوره متعهدند كه در طول اجرای برنامه ارزشها و اصول نظام جمهوري اسلامي ايران را رعايت نموده و زمينه­هاي رشد و تعالي انساني را براي دانشجويان اين برنامه آموزشي بر مبناي فرهنگ غني اسلامي و با در نظر گرفتن كرامت انساني فراهم نمايند و بتوانند پزشکانی متعهد به اسلام و پایبند به موازین علمی تربیت نمایند.
فراهم کردن مبنایی برای ارزشیابی نحوه پیاده سازی و اجرای برنامه همراه با تعیین میزان دستیابی به کلیه اهداف آموزشی و فراهم نمودن ساز و کار مناسب جهت ارزیابی توانمندی دانش آموختگان از جمله مهمترین رسالت برنامه خواهد بود.
