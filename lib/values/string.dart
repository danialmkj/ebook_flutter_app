تاریخچه طب در ایران:سابقه آموزش آکادمیک طب در ایران، به قبل از ظهور اسلام باز می گردد. مدرسه‌ و بیمارستان‌ عظیم‌ و نامدار «جندی‌ شاپور» در قرن ششم میلادی، هم‌ مدرسه‌ و مركز تحصیل‌ طب و فلسفه‌ بود، و هم‌ بیمارستان‌ و دارالشفای‌بیمارانی‌ كه‌ بدان‌ جا رجوع‌ می‌كردند. دانشگاه جندی شاپور از مهمترین مراكز آموزشی و تحقیقی دنیای آن زمان بود كه تعداد زیادی دانشمند و پزشك در آن مشغول به تدریس، تحصیل و طبابت بودند. در این مركز علاوه بر كتب تالیف شده دانشمندان ایرانی بسیاری از كتابهای یونانی و هندی را به پهلوی ترجمه كرده و آن ها را تعلیم می‌دادند. با مطالعه و بررسی این دانشگاه می‌توان به قدمت تعلیم و تعلم رسمی طب ‌در ایران پی برد. همچنین ابداع روش درمان بیمارستانی را باید تا اندازه زیادی از آن ایرانیان دانست. بیمارستانهای دوره اسلامی ‌اغلب براساس نمونه ها و اصول بیمارستانی جندی شاپور ساخته شده بودند.
بیمارستان های معروفعضدالدولهدرشیرازوبغداد،و بیمارستانهای بعدی دردمشق براساس نمونه جندی شاپور بنا گردیده بودند. نخستین فرآورده دارویی اسلامی ‌نیز از این مركز بزرگ پزشكی جهان بوده است. جندی شاپور به هنگام فتوحات اعراب مهمترین مركز پزشكی جهان بوده است. این دانشگاه تا قرنها از مشهورترین دانشگاههای جهان بود.
پس از اسلام با شکوفایی تمدن اسلامی و باروری این تمدن در ایران، پزشکان ایرانی همچنان طلایه دار آموزش آکادمیک طب و تدوین کتابهای مرجع بودند. مدرس(محل تدریس) ابن سینا، به عنوان کهن ترین آکادمی آموزش طب که کتاب درسی معتبر(قانون در طب) را نیز منتشر کرده است همچنان پابرجاست کما اینکه تا قرنها این کتاب منبع درسی معتبر برای اطبا در سراسر دنیا به شمار میرفت.
آموزش طب نوین در ایران تا قبل از تشکيل مدرسه دارالفنون، سازماندهی خاصی نداشت و کتاب قانون ابن سينا و شرح اسباب نفيسي، کتب معتبر و کلاسيک به شمار مي‌رفتند. براي تحصيل طب محلی معین وجود نداشت و فراگيرندگان پس از طي مقدمات اوليه(خواندن دروس صرف و نحو و حکمت الهي در مطب پزشکان معروف قديمي)، به فراگرفتن حکمت طبيعي(طب وغيره) مي‌پرداختند و بدون هيچ مانعي مي‌توانستند پس از گذراندن مقدمات اوليه و کسب اطلاعات مختصري در طب، به عنوان پزشک به درمان بيماران مشغول شوند. اين وضع تا زمان سلطنت ناصرالدين شاه و صدارت اميرکبير ادامه داشت. در اين دوره به دستور ناصرالدين شاه، دکتر کلوکه، حکيم باشي دربار براي ارتقاي آموزش طبابت به آموزش تعدادي از افراد همت گمارد تا اينکه در سال 1266، اميرکبير قسمتي از ارک شاهي را براي تاسيس مدرسه دارالفنون در نظر گرفت و در سال 1267 قسمتي از آن آماده شد. در سال 1290، ناصرالدين شاه دستور ساخت بيمارستان را صادر کرد و اولين بيمارستان در ايران(بيمارستان سينا)، در سال 1298 به رياست مرحوم ناظم الاطباء افتتاح شد و سپس دانشکده‌هاي ديگري نيز از مدرسه دارالفنون منشعب گرديد.
در سال 1297، تشکيلات مدرسه طب از دارالفنون جدا شد و مرحوم لقمان الدوله ادهم به رياست آن برگزيده شد و از همين دوره، بخصوص از سال 1307، آموزش طب(در مدرسه طب) نظم و قوام بيشتري پيدا کرد و به دوره‌هاي آموزش علوم پايه و باليني تقسيم شد. در سال 1313 قانون تاسيس دانشگاه تهران به تصويب مجلس رسيد و هريک از مدارس، دانشکده ناميده شدند که يکي از آنها دانشکده طب بود. اکنون پس از گذشت قریب 100 سالاز تاسیس اولین مدرسه پزشکی در ایران، تعداد 63 دانشکده پزشکی در سراسر کشور مسئولیتآموزش دانشجویان پزشکی عمومی را برعهده دارند.
