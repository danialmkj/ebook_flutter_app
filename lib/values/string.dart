روش­ها و فنون آموزشي:
در اين دوره، از روش­ها و فنون آموزشي مختلف متناسب با اهداف یادگیری هر درس و امکانات و شرایط دانشکدهبهره گرفته خواهد شد.
تاکید برنامه بر تناسب روشها و فنون با اهداف و زمینه های یادگیری است و از این رو، روش یا فن خاصی در کل برنامه ملی تجویز نمی شود. با این حال، توصیه می شود در استفاده از روش یا فنون آموزشی علاوه بر وجود شواهد در مورد پیامدهای آموزشی هر روش یا فن، به پیامدهای پرورشی آن و نیز آمادگی دانشجویان و مدرسان برای اجرای مناسب، همچنین مسائل اقتصادی و اجرایی کاربرد این روشها نیز توجه شود. در مواردی توصیه های لازم در مورد روش خاص برای برخی دروس در شناسنامه معرفی دروس ارائه می شود. همچنین در استانداردهای برنامه روشهای آموزشی برای ارائه برنامه بیشتر توضیح داده شده است.
