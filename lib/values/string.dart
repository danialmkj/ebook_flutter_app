انتظارات اخلاقي از فراگيران:
از دانشجویان پزشکی انتظار می رود:
-          در مراقبت از بيمار، منشور حقوق بيمار را دقيقا رعايت نمايند.
-          مقررات مرتبط با حفاظت و ايمني بيمار(Patient safety) را دقيقا رعايت نمايند(این مقررات توسط گروه آموزشی مربوطه تدوین و در اختیار فراگیران قرار خواهد گرفت).
-          كدهاي اخلاقي مرتبط با مادر، جنين و نوزادان را به دقت مراعات کنند(اين كدها از طرف گروه آموزشي در اختيار فراگيران قرار خواهد گرفت).
-          مقررات مرتبط با حفاظت و ايمني داروها(Drug Safety) شامل ترکیبات شیمیایی و غیر شیمیایی مرتبط را دقیقا رعایت نماید. (این مقررات توسط گروه آموزشی مربوطه تدوین و در اختیار فراگیران قرار خواهد گرفت).
-          مقررات پوشش حرفه اي(Dress code) را رعايت نمايند.
-          مقررات كار با حيوانات آزمايشگاهي را رعایت نماید.
-          به سوگند نامه حرفه خود متعهد و پايبند باشند.
-          از منابع و تجهيزاتي که تحت هر شرايط با آن کار مي کنند محافظت نمايند.
-          به استادان، کارکنان، همكاران و فراگيران ديگر احترام بگذارند و در ايجاد جو صميمي و احترام آميز در محيط کار مشارکت نمايند.
-          در نقد برنامه ها، ملاحظات اخلاق اجتماعي و حرفه اي را رعايت کنند.
-          در انجام پژوهش هاي مربوط به رشته، نکات اخلاق پژوهش را رعايت نمايند.
