دروس انتخابی شامل مباحث غیر الزامی(non core) برنامه است که به دانشگاهها و دانشجویان امکان می دهد حسب شرایط دانشگاه، نیازهای خاص منطقه و نیز علائق گروههای آموزشی و دانشجویان، محتوا و فرصتهای یادگیری متنوعی را به عنوان مکمل برای کمک به تحقق توانمندیهای مورد انتظار از پزشکان عمومی ارائه دهند. مجموع واحدهای انتخابی اختصاصی در طول دوره پزشکی عمومی، 16 واحد است: 
تعداد واحد انتخابی اختصاصی که دانشجو باید تا قبل از آزمون پیش کارورزی گذرانده باشد: 4 واحد 
تعداد واحد انتخابی اختصاصی(کارورزی)که باید در مرحله کارورزی حسب برنامه دانشگاه و انتخاب کارورز طی شود:12 واحد 
دروس شناور: 
الف) دروس شناور بین مرحله علوم پایه و مقدمات بالینی: معادل 15 واحد از دروس پایه(نام دروس در جدول مشخص شده است) می تواند در مرحله علوم پایه یا مقدمات بالینی ارائه شود. این دروس در آزمون جامع علوم پایه لحاظ نخواهد شد. به این ترتیب، گذراندن این دروس برای شرکت در آزمون جامع علوم پایه الزامی نیست و از طرف دیگر، گذراندن این دروس نیز بر خلاف دروس مرحله مقدمات بالینی، مشروط به موفقیت در آزمون جامع علوم پایه نخواهد بود. 
ب) دروس شناور بین مرحله مقدمات بالینی یا کارآموزی: معادل 7 واحد از دروس تخصصی(نام دروس در جدول مشخص شده است) میتواند در مرحله مقدمات بالینی یا کارآموزی ارائه شود. 
توجه:
اختیار تغییر در محتوای برنامه های آموزشی مبتنی بر نیازهای سلامت منطقه مصوبه شصت و دومین جلسه شورای عالی برنامه ریزی علوم پزشکی مورخ 20/10/1394(ابلاغیه شماره 415/500/د مورخ 12/2/95) در طول برنامه لحاظ شده است. 


